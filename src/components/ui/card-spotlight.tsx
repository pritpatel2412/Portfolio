
"use client";

import { useMotionValue, motion, useMotionTemplate } from "motion/react";
import React, { useState } from "react";
import { CanvasRevealEffect } from "./canvas-reveal-effect"; 
import { cn } from "../../lib/utils";

const CardSpotlight = ({
  children,
  radius = 100,
  color = "#262626",
  className,
  onMouseEnter,
  onMouseLeave,
  ...props
}) => {
  const mouseX = useMotionValue(0);
  const mouseY = useMotionValue(0);

  function handleMouseMove({ currentTarget, clientX, clientY }) {
    let { left, top } = currentTarget.getBoundingClientRect();
    mouseX.set(clientX - left);
    mouseY.set(clientY - top);
  }

  const [isHovering, setIsHovering] = useState(false);
  const handleEnter = () => {
    setIsHovering(true);
    if (onMouseEnter) onMouseEnter();
  };
  const handleLeave = () => {
    setIsHovering(false);
    if (onMouseLeave) onMouseLeave();
  };

  return (
    <div
      className={cn(
        "group/spotlight rounded-full overflow-hidden relative border border-neutral-800 bg-black dark:border-neutral-800 flex items-center justify-center",
        className
      )}
      onMouseMove={handleMouseMove}
      onMouseEnter={handleEnter}
      onMouseLeave={handleLeave}
      {...props}
    >
      <motion.div
        className="pointer-events-none absolute z-0 -inset-px opacity-0 transition duration-300 group-hover/spotlight:opacity-100"
        style={{
          backgroundColor: color,
          maskImage: useMotionTemplate`
            radial-gradient(
              ${radius}px circle at ${mouseX}px ${mouseY}px,
              white,
              transparent 80%
            )
          `,
        }}
      >
        {isHovering && (
          <CanvasRevealEffect
            animationSpeed={5}
            containerClassName="bg-transparent absolute inset-0 pointer-events-none"
            colors={[
                [59, 130, 246],
                [139, 92, 246],
              ]}
              dotSize={3.5}
          />
        )}
      </motion.div>
      <div className="relative z-20 p-1 text-center">{children}</div>
    </div>
  );
};

